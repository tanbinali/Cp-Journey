using ll = long long;

void solve() {
    int n;
    cin >> n;

    vector <ll> v;
    while(n--){
        int val;
        cin >> val;
        v.push_back(val);
    }
    ll sum = 0;
    for(ll x:v){
        sum+=x;
    }
    if(sum>=0) cout << sum;
    else cout << sum*-1;
}

